<template>
  <highcharts ref="chart" :options="options"></highcharts>
</template>

<script>
import { Chart } from "highcharts-vue";

function kilobyteFormatter(value, digits = 4) {
  return (value / 1024).toLocaleString(undefined, {
    maximumFractionDigits: digits
  });
}

function gasFormatter(value, digits = 6) {
  return (value / 10 ** 6).toLocaleString(undefined, {
    maximumFractionDigits: digits
  });
}


function floorFormatter(value, digits = 6) {
  return (value).toLocaleString(undefined, {
    maximumFractionDigits: digits
  });
}

function defaultFormatter(value) {
  return value;
}

export default {
  name: "ColumnChart",
  props: {
    data: Array,
    title: String,
    name: String,
    formatter: String
  },
  components: {
    highcharts: Chart
  },
  computed: {
    labelFormatterFunction() {
      if (this.formatter === "kilobyte") {
        return function() {
          return kilobyteFormatter(this.total, 0);
        };
      } else if (this.formatter === "gas") {
        return function() {
          return gasFormatter(this.total, 0);
        };
      }
      return function() {
        return defaultFormatter(this.total);
      };
    },
    tooltipFormatterFunction() {
      if (this.formatter === "kilobyte") {
        return function() {
          let value = kilobyteFormatter(this.y);
          return `<span style="color:${this.color}">●</span> ${this.series.name}: <b>${value} kB</b><br/>`;
        };
      } else if (this.formatter === "gas") {
        return function() {
          let value = floorFormatter(this.y, 0);
          return `<span style="color:${this.color}">●</span> ${this.series.name}: <b>${value}</b><br/>`;
        };
      }
      return function() {
        let value = defaultFormatter(this.y);
        return `<span style="color:${this.color}">●</span> ${this.series.name}: <b>${value}</b><br/>`;
      };
    },
    options() {
      if (this.data == null) return {};
      return {
        xAxis: {
          type: "datetime",
          tickmarkPlacement: "off",
          title: {
            enabled: false
          },
          tickWidth: 0,
          lineWidth: 0,
          endOfTick: false,
          gridLineWidth: 0,
          tickPixelInterval: 65,
          dateTimeLabelFormats: {
            millisecond: "%H:%M:%S.%L",
            second: "%H:%M:%S",
            minute: "%H:%M",
            hour: "%H:%M",
            day: "%e %b",
            week: "%e %b",
            month: "%b '%y",
            year: "%Y"
          },
          labels: {
            style: {
              fontSize: "12px"
            }
          }
        },
        yAxis: {
          enabled: false,
          title: {
            text: ""
          },
          gridLineWidth: 0,
          labels: {
            enabled: false
          },
          stackLabels: {
            enabled: true,
            style: {
              fontWeight: "medium",
              color: "var(--v-secondary-base)",
              fontSize: "12px",
              textOutline: "none"
            },
            formatter: this.labelFormatterFunction
          }
        },
        title: {
          text: this.title,
          useHTML: true,
          floating: true,
          y: 20,
          style: {
            color: "var(--v-text-base)"
          }
        },
        tooltip: {
          followTouchMove: false,
          headerFormat: '<span style="font-size: 12px">{point.key}</span><br/>',
          backgroundColor: "var(--v-sidebar-base)",
          shadow: false,
          borderWidth: 0,
          xDateFormat: "%B %Y",
          useHTML: true,
          style: {
            color: "var(--v-text-base)",
            pointerEvents: "none",
            fontSize: "14px"
          },
          pointFormatter: this.tooltipFormatterFunction
        },
        legend: {
          enabled: false
        },
        credits: {
          enabled: false
        },
        series: [
          {
            type: "column",
            data: this.data,
            color: "var(--v-primary-base)",
            name: this.name,
            borderColor: "transparent",
            label: {}
          }
        ],
        chart: {
          backgroundColor: "transparent",
          plotBackgroundColor: "transparent",
          marginTop: 50,
          style: {
            fontFamily: "Roboto Condensed, sans-serif"
          }
        },
        plotOptions: {
          column: {
            stacking: "normal"
          }
        }
      };
    }
  }
};
</script>